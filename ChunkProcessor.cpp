#include "ChunkProcessor.hpp"
#include "Constants.hpp"
#include "sha256.hpp"

#include <cstring>

// Функция для проверки, является ли символ буквой.
// В рамках тестового задания мы предполагаем однобайтовую кодировку (например, Windows-1251).
// Мы используем собственную реализацию вместо std::isalpha, чтобы:
// 1. Избежать Undefined Behavior (UB) при передаче отрицательных значений (char для кириллицы).
// 2. Исключить накладные расходы на проверку текущей локали (locale), что критично для скорости.
bool ChunkProcessor::IsAlpha(char c)
{
    // Явное приведение к unsigned char необходимо, так как обычный char может быть знаковым,
    // и символы с кодом > 127 будут отрицательными числами.
    unsigned char uc = static_cast<unsigned char>(c);
    return (uc >= 'a' && uc <= 'z') || 
           (uc >= 'A' && uc <= 'Z') ||
           (uc >= 0xC0 && uc <= 0xFF) || // Диапазон заглавных и строчных букв А-Я, а-я в кодировке Windows-1251
           (uc == 0xA8) || (uc == 0xB8); // Отдельные коды для букв Ё и ё в Windows-1251
}

// Функция для приведения символа к нижнему регистру.
// Аналогично IsAlpha, реализована вручную для максимальной производительности
// и независимости от локали ОС.
char ChunkProcessor::ToLower(char c)
{
    unsigned char uc = static_cast<unsigned char>(c);
    // Для латиницы: смещаем код символа на разницу между 'a' и 'A' (обычно это 32 или 0x20)
    if (uc >= 'A' && uc <= 'Z') return static_cast<char>(uc + ('a' - 'A'));
    // Для кириллицы Windows-1251: строчные буквы находятся ровно на 0x20 дальше заглавных
    if (uc >= 0xC0 && uc <= 0xDF) return static_cast<char>(uc + 0x20); // А-Я -> а-я
    // Отдельная обработка буквы Ё -> ё
    if (uc == 0xA8) return static_cast<char>(0xB8); 
    return c;
}

// Основной метод обработки куска файла (чанка) в отдельном потоке.
// Параметры:
// data - константный указатель на начало файла (Memory-Mapped).
// size - общий размер файла в байтах.
// start - начальный индекс (байт), с которого этот поток должен начать чтение.
// end - конечный индекс, на котором этот поток должен остановиться.
std::unordered_map<std::string, uint64_t> ChunkProcessor::Process(const char* const Data, size_t Size, size_t Start, size_t End)
{
    // Локальный словарь для хранения частоты слов.
    // Каждый поток использует СВОЙ локальный словарь, что позволяет полностью
    // избавиться от использования мьютексов (std::mutex) в горячем цикле.
    // Это ключевое решение архитектуры Map-Reduce, дающее максимальное распараллеливание.
    std::unordered_map<std::string, uint64_t> LocalMap;

    // корректировка границ чанка
    // Так как файл делится на равные куски математически (file_size / num_threads),
    // граница `start` почти наверняка попадет на середину слова.
    // Если `start` находится внутри слова (или прямо перед ним стоит буква),
    // значит, это слово уже будет полностью прочитано ПРЕДЫДУЩИМ потоком,
    // потому что предыдущий поток читает слово до конца, даже если оно выходит за его `end`.
    if (Start > 0 && IsAlpha(Data[Start]) && IsAlpha(Data[Start - 1]))
    {
        // Пропускаем символы, пока не найдем конец текущего слова (переходим на пробел/пунктуацию)
        while (Start < Size && IsAlpha(Data[Start]))
        {
            Start++;
        }
    }

    // Стековые буферы для хранения предыдущего и текущего слова.
    // Использование стековых массивов вместо std::string(heap allocation) внутри цикла
    // дает прирост производительности (Zero-Copy парсинг).
    char PrevWord[Config::WordBufferSize];
    size_t PrevLen = 0;

    // поиск предыдущего слова для контекста SHA-256
    // По условию задачи, для вычисления хэша нам нужно конкатенировать (предыдущее + текущее) слово.
    // Чтобы первому слову в нашем чанке было с чем склеиваться, мы "отматываем" указатель назад
    // за пределы нашего чанка и находим последнее слово из предыдущего чанка.
    size_t SearchPos = Start;
    
    // Отматываем назад через пробелы и знаки препинания
    while (SearchPos > 0 && !IsAlpha(Data[SearchPos - 1]))
    {
        SearchPos--;
    }

    // Если мы не уперлись в начало файла, значит предыдущее слово существует
    if (SearchPos > 0)
    {
        size_t PreviousEnd = SearchPos - 1; // Конец предыдущего слова
        size_t PreviousStart = PreviousEnd;
        
        // Отматываем назад до начала этого слова
        while (PreviousStart > 0 && IsAlpha(Data[PreviousStart - 1]))
        {
            PreviousStart--;
        }
        
        // Копируем найденное слово в буфер, заодно переводя его в нижний регистр
        for (size_t i = PreviousStart; i <= PreviousEnd; ++i)
        {
            if (PrevLen < Config::MaxWordLength)
            {
                PrevWord[PrevLen++] = ToLower(Data[i]);
            }
        }
    }

    size_t Pos = Start;
    char CurrentWord[Config::WordBufferSize];

    while (Pos < End)
    {
        // Пропускаем все символы-разделители (не-буквы)
        while (Pos < Size && !IsAlpha(Data[Pos]))
        {
            Pos++;
        }
        
        // Важная проверка: если начало следующего целого слова вышло за границу нашего чанка (`end`),
        // мы прерываем цикл. Это слово будет обработано следующим потоком (как его первое слово).
        // Это гарантирует, что ни одно слово не будет посчитано дважды.
        if (Pos >= End)
        {
            break;
        }

        // Читаем текущее слово
        size_t CurrentLen = 0;
        while (Pos < Size && IsAlpha(Data[Pos]))
        {
            if (CurrentLen < Config::MaxWordLength)
            {
                CurrentWord[CurrentLen++] = ToLower(Data[Pos]);
            }
            Pos++;
        }

        // Вычисление SHA-256
        // Если у нас есть пара слов (длина обоих > 0), вычисляем хэш
        if (PrevLen > 0 && CurrentLen > 0)
        {
            uint8_t Hash[32]; // SHA-256 всегда возвращает 32 байта (256 бит)
            SHA256_CTX Ctx;
            sha256_init(&Ctx);
            
            // Вместо того чтобы склеивать две строки в памяти (вызывая аллокацию),
            // мы "скармливаем" их алгоритму последовательно.
            // Математически это абсолютно эквивалентно хэшированию конкатенированной строки.
            sha256_update(&Ctx, reinterpret_cast<const uint8_t*>(PrevWord), PrevLen);
            sha256_update(&Ctx, reinterpret_cast<const uint8_t*>(CurrentWord), CurrentLen);
            sha256_final(&Ctx, Hash);

            // Проверка условия задачи
            // По заданию хэш должен заканчиваться на символ от 0 до 7 в шестнадцатеричном виде.
            // Hex-представление формируется по полубайтам (nibbles - 4 бита).
            // Последний символ Hex-строки соответствует младшим 4 битам последнего (31-го) байта хэша.
            // Бинарная маска 0x0F (0000 1111) отсекает старшие биты.
            // Если результат <= 7 (0111 в бинарном), условие выполнено!
            // Это избавляет нас от ресурсоемкой конвертации байтов в 64-символьную Hex-строку.
            if ((Hash[31] & 0x0F) <= 7)
            {
                // Только если условие выполнено, мы конвертируем стековый буфер в std::string 
                // и увеличиваем счетчик в словаре.
                LocalMap[std::string(CurrentWord, CurrentLen)]++;
            }
        }

        // Сдвиг контекста
        // Текущее слово становится предыдущим для следующей итерации цикла.
        // Используем быстрый memcpy вместо присваивания строк.
        if (CurrentLen > 0)
        {
            memcpy(PrevWord, CurrentWord, CurrentLen);
            PrevLen = CurrentLen;
        }
    }

    // Возвращаем локальный словарь. Он будет передан в главный поток
    // через std::future, что избавляет нас от мьютексов при передаче результата.
    return LocalMap;
}
